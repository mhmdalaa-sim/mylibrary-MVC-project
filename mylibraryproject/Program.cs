using Microsoft.EntityFrameworkCore;
using mylibraryproject.filters;
using mylibraryproject.Models.Data;
using mylibraryproject.Models.Repos;

namespace mylibraryproject
{
    public class Program
    {

        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddControllersWithViews();

          

            builder.Services.AddDbContext<ApplicationDbContext>(a =>
        {
            a.UseSqlServer(builder.Configuration.GetConnectionString("conn"));

        });

            builder.Services.AddScoped<IBookRepository, BookRepository>();

            builder.Services.AddScoped<CustomActionFilter>();

            builder.Services.AddDistributedMemoryCache();
            builder.Services.AddSession(options =>
            {
                options.IdleTimeout = TimeSpan.FromMinutes(30);
                options.Cookie.HttpOnly = true;
                options.Cookie.IsEssential = true;
            });


            var app = builder.Build();

         

            // Configure the HTTP request pipeline.
            if (!app.Environment.IsDevelopment())
            {
                app.UseExceptionHandler("/Home/Error");
                // The default HSTS value is 30 days. You may want to change this for production scenarios, see https://aka.ms/aspnetcore-hsts.
                app.UseHsts();
            }

            app.UseHttpsRedirection();
            app.UseStaticFiles();

            app.UseRouting();

            app.UseSession();

            app.UseAuthorization();

            app.MapControllerRoute(
                name: "default",
                pattern: "{controller=Book}/{action=Index}/{id?}");

            app.Run();
        }
    }
}